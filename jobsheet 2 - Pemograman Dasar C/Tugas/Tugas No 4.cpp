#include <stdio.h>

int main() {
	//Deklarasi variabel
	double panjang = 10.0;
	double  lebar = 5.0;
	double luas ;
	
	//Menghitung luas persegi panjang
	luas = panjang *  lebar;
	
	// Menampilkan hasil perhitungan luas persegi panjang
	printf("panjang = %.f cm\n", panjang);
	printf ("Lebar = %.f cm\n", lebar);
	printf("Maka, Luas Persegi Panjang adalah %.2lf cm^2\n", luas);
	
	return 0;
}
